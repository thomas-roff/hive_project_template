double	ft_sin(double x)
{
	double	term;
	double	sum;
	int		i;

	term = x;
	sum = x;
	i = 1;
	while (i < 10)
	{
		term *= -x * x / ((2 * i) * (2 * i + 1));
		sum += term;
		i++;
	}
	return (sum);
}
