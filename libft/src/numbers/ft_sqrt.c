double	ft_sqrt(double nbr)
{
	double	res;
	int		i;

	res = nbr;
	i = 0;
	if (nbr <= 0)
		return (0);
	while (i < 20)
	{
		res = 0.5 * (res + nbr / res);
		i++;
	}
	return (res);
}
