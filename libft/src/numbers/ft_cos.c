double	ft_cos(double x)
{
	double	term;
	double	sum;
	int		i;

	term = 1.0;
	sum = 1.0;
	i = 1;
	while (i < 10)
	{
		term *= -x * x / ((2 * i - 1) * (2 * i));
		sum += term;
		i++;
	}
	return (sum);
}
