#include "../../inc/libft.h"

int	ft_isarr(char **arr, int (f)(int))
{
	int	i;
	int	j;

	i = 0;
	while (arr[i])
	{
		j = 0;
		while (arr[i][j])
		{
			if (!f(arr[i][j]))
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
